const ProtectedRoute: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const { currentUser } = useAuth();
  return currentUser ? <>{children}</> : <Navigate to="/login" />;
};

const AppRoutes: React.FC = () => {
  const { currentUser } = useAuth();

  return (
    <Routes>
      {/* Public routes */}
      <Route 
        path="/login" 
        element={currentUser ? <Navigate to="/" /> : <Login />} 
      />
      <Route 
        path="/register" 
        element={currentUser ? <Navigate to="/" /> : <Register />} 
      />
      {/* <Route path="/debug" element={<FirebaseDebug />} /> */}
      <Route path="/test-submission" element={<SubmissionTest />} />
      
      {/* Protected routes */}
      <Route
        path="/*"
        element={
          <ProtectedRoute>
            <Layout>
              <Routes>
                <Route path="/" element={<Dashboard />} />
                <Route path="/dashboard" element={<Dashboard />} />
                <Route path="/lessons/new" element={<CreateLesson />} />
                <Route path="/lessons/:lessonId" element={<LessonPage />} />
                <Route path="/lessons/:lessonId/exercises/new" element={<CreateExercise />} />
                <Route path="/exercise/:exerciseId" element={<ExercisePage />} />
                <Route path="/exercise/:exerciseId/edit" element={<EditExercise />} />
                <Route path="/exercise/:exerciseId/submissions" element={<ExerciseSubmissions />} />
                <Route path="/exercise/:exerciseId/student/:studentId/submissions" element={<StudentSubmissions />} />
                <Route path="/all-lessons" element={<AllLessons />} />
                <Route path="/submission/:submissionId" element={<SubmissionReview />} />
                <Route path="/teacher-management" element={<TeacherManagement />} />
                {/* Add more protected routes here */}
              </Routes>
            </Layout>
          </ProtectedRoute>
        }
      />
    </Routes>
  );
};

function App() {
  return (
    <ThemeProvider theme={theme}>
      <CssBaseline />
      <AuthProvider>
        <Router>
          <AppRoutes />
        </Router>
      </AuthProvider>
    </ThemeProvider>
  );
}

export default App;
